public class Jugador {
  private String name;
  private int KDA;
  private int Games;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getKDA() {
        return KDA;
    }

    public void setKDA(int KDA) {
        this.KDA = KDA;
    }

    public int getGames() {
        return Games;
    }

    public void setGames(int games) {
        this.Games = games;
    }


}
